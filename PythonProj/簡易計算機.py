a=int(input('請輸入a值：'))
b=int(input('請輸入b值：'))
math=input('請輸入運算符號:')

if math==('+'):
    sum=a+b
    print('您的答案為:',sum)
elif math==('-'):
    sum=a-b
    print('您的答案為:',sum)
elif math==('*'):
    sum=a*b
    print('您的答案為:',sum)
elif math==('/'):
    sum=a/b
    print('您的答案為:',sum)
elif math==('//'):
    sum=a//b
    print('您的答案為:',sum)
elif math==('%'):
    sum=a%b
    print('您的答案為:',sum)
elif math==('**'):
    sum=a**b
    print('您的答案為:',sum)
else:
    print('請輸入正確的符號:')
    

